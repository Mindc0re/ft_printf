#include "includes/libft.h"

void	ft_printhex(unsigned char n)
{
	if (n >= 16)
		ft_printhex(n / 16);
	if ((n % 16) > 10)
		ft_putchar((n % 16) - 10 + 'A');
	else
		ft_putchar((n % 16) + '0');
}
